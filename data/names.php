function fix_name($name)
{
    if (strpos($name, 'of') !== FALSE)
    {
        $name = str_replace('\'s', '', $name);
    }
    $name = str_replace('  ', ' ', $name);
    return $name;
}

function armor_name($type)
{
    global $namepart1, $namepart2_armor_light, $namepart2_armor_heavy;
    return fix_name(arr($namepart1) .
        ($type === H || $type === M ? arr($namepart2_armor_heavy) : arr($namepart2_armor_light)));
}

function shield_name()
{
    global $namepart1, $namepart2_shield;
    return fix_name(arr($namepart1) . arr($namepart2_shield));
}

function weapon_name()
{
    global $namepart1, $namepart2_weapon;
    return fix_name(arr($namepart1) . arr($namepart2_weapon));  
}

