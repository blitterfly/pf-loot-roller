class Scroll
{
    public $level;
    public $name;
    public $cost;
    
    function __construct($level, $name, $cost = 0)
    {
        $this->level = $level;
        $this->name = $name;
        
        if ($cost === 0)
        {
            switch ($level)
            {
                case 1: $this->cost = 25; break;
                case 2: $this->cost = 150; break;
                case 3: $this->cost = 375; break;
                case 4: $this->cost = 700; break;
                case 5: $this->cost = 1125; break;
                case 6: $this->cost = 1650; break;
                case 7: $this->cost = 2275; break;
                case 8: $this->cost = 3000; break;
                case 9: $this->cost = 3825; break;
                default: $this->cost = 13; break;
            }
        }
        else
        {
            $this->cost = $cost;
        }
    }
}

